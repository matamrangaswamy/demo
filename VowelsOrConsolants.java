public class VowelsOrConsolants {
	public static void main(String[] args) {
		char ch = 't';
		if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' || ch == 'A' || ch == 'E' || ch == 'I'
				|| ch == 'O' || ch == 'U') {
			System.out.println(ch + " Is a vowels");
		} else
			System.out.println(ch + " Is a consolants");
	}
}
